void keyboard_process()
{
	uint8_t code;
	uchar_t ascii = 0;

	inbyte(0x60, &code);

	if (code & 0x80) {
		// breakcode
		//
	} else if (code == KBV_CAPSLOCK) {
		keymap[KBV_CAPSLOCK].val1 = !keymap[KBV_CAPSLOCK].val1;

	} else {
		if (keymap[KBV_CAPSLOCK].val1)
			ascii = keymap[code].val2;
		else
			ascii = keymap[code].val1;

		printf("%c", ascii);
	}

}
